CREATE TABLE D_VEHICLES
(
  VEHICLE_TK BIGINT NOT NULL PRIMARY KEY
, VERSION INT
, DATE_FROM DATETIME
, DATE_TO DATETIME
, VEHICLE_ID VARCHAR(36)
, VEHICLE_REGISTRATION VARCHAR(255)
, VEHICLE_CREATED_BY VARCHAR(36)
, VEHICLE_DELETED INT
, VEHICLE_MAKE VARCHAR(255)
, VEHICLE_MODEL VARCHAR(255)
, VEHICLE_FUEL_TYPE VARCHAR(100)
)
;CREATE INDEX IDX_D_VEHICLES_LKP ON D_VEHICLES(VEHICLE_ID)
;
CREATE INDEX IDX_D_VEHICLES_TK ON D_VEHICLES(VEHICLE_TK)
;
